<h1>About Macintosh Garden FTP Search</h1>
<dt>What does this do?
<dd>It searches a cached version of the files on the Macintosh Garden FTP server and provides FTP links to download them. Nothing is hosted here.
<dt>Why do this?
<dd>To enable vintage Mac 68k computers access to the Macintosh Garden FTP server quickly. The file listing is too large to load for an old machine.
